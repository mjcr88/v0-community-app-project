import { createClient } from "@/lib/supabase/server"
import { redirect } from "next/navigation"
import { NeighboursPageClient } from "./neighbours-page-client"
import { applyPrivacyFilter, type UserWithPrivacy } from "@/lib/privacy-utils"

export default async function NeighboursPage({ params }: { params: Promise<{ slug: string }> }) {
  const { slug } = await params
  const supabase = await createClient()

  const {
    data: { user },
  } = await supabase.auth.getUser()

  if (!user) {
    redirect(`/t/${slug}/login`)
  }

  const { data: currentResident } = await supabase
    .from("users")
    .select("id, tenant_id, role, family_unit_id")
    .eq("id", user.id)
    .eq("role", "resident")
    .single()

  if (!currentResident) {
    redirect(`/t/${slug}/login`)
  }

  // Get all residents with privacy settings
  const { data: residents, error: residentsError } = await supabase
    .from("users")
    .select(
      `
      id,
      first_name,
      last_name,
      email,
      phone,
      profile_picture_url,
      journey_stage,
      estimated_move_in_date,
      birth_country,
      current_country,
      languages,
      preferred_language,
      birthday,
      family_unit_id,
      family_units!users_family_unit_id_fkey (
        id,
        name,
        profile_picture_url
      ),
      lots (
        id,
        lot_number,
        neighborhoods (
          id,
          name
        )
      ),
      user_interests (
        interests (
          id,
          name
        )
      ),
      user_skills (
        skills (
          id,
          name
        ),
        open_to_requests
      ),
      user_privacy_settings (
        show_email,
        show_phone,
        show_birthday,
        show_birth_country,
        show_current_country,
        show_languages,
        show_preferred_language,
        show_journey_stage,
        show_estimated_move_in_date,
        show_profile_picture,
        show_neighborhood,
        show_family,
        show_family_relationships,
        show_interests,
        show_skills,
        show_open_to_requests
      )
    `,
    )
    .eq("tenant_id", currentResident.tenant_id)
    .eq("role", "resident")
    .order("first_name")

  console.log("[DEBUG] Neighbours page - tenant_id:", currentResident.tenant_id)
  console.log("[DEBUG] Neighbours page - residents query error:", residentsError)
  console.log("[DEBUG] Neighbours page - residents found:", residents?.length, residents?.map(r => `${r.first_name} ${r.last_name}`))

  // Get all families
  const { data: families } = await supabase
    .from("family_units")
    .select(
      `
      id,
      name,
      profile_picture_url,
      primary_contact_id,
      users!users_family_unit_id_fkey (
        id,
        first_name,
        last_name,
        lots (
          id,
          lot_number,
          neighborhoods (
            id,
            name
          )
        )
      ),
      pets (
        id
      )
    `,
    )
    .eq("tenant_id", currentResident.tenant_id)
    .order("name")

  // Get all interests for filtering
  const { data: allInterests } = await supabase
    .from("interests")
    .select("id, name")
    .eq("tenant_id", currentResident.tenant_id)
    .order("name")

  // Get all neighborhoods for filtering
  const { data: neighborhoods } = await supabase
    .from("neighborhoods")
    .select("id, name")
    .eq("tenant_id", currentResident.tenant_id)
    .order("name")

  // Get neighbor lists
  const { getNeighborLists } = await import("@/app/actions/neighbor-lists")
  const { data: neighborLists } = await getNeighborLists(currentResident.tenant_id)

  // Check if current user is admin
  const isTenantAdmin = currentResident.role === "tenant_admin" || currentResident.role === "super_admin"

  // Apply privacy filter to residents
  const filteredResidents = residents?.map((resident) => {
    const isFamilyMember =
      !!currentResident.family_unit_id &&
      !!resident.family_unit_id &&
      currentResident.family_unit_id === resident.family_unit_id

    // Cast to UserWithPrivacy to satisfy type checker (supabase types vs app types)
    return applyPrivacyFilter(resident as unknown as UserWithPrivacy, user.id, isFamilyMember, isTenantAdmin)
  })

  return (
    <NeighboursPageClient
      residents={(filteredResidents as any) || []}
      families={families || []}
      neighborhoods={neighborhoods || []}
      allInterests={allInterests || []}
      neighborLists={neighborLists || []}
      tenantSlug={slug}
      currentUserFamilyId={currentResident.family_unit_id || null}
      currentTenantId={currentResident.tenant_id}
      isTenantAdmin={isTenantAdmin}
    />
  )
}
